      <p><strong>Nature Primary School</strong> has come a long way since being founded in 1981 as one of the first British international schools in Malaysia.
        From its original location in the Lake Gardens of Kuala Lumpur, NPS now is based in Mont Kiara and Desa Sri Hartamas in Kuala Lumpur.
        GIS is one of the largest international schools in Malaysia, educating over 2,000 students. Explore the timeline below to learn about our milestones over the last 65 years,
        delivering excellent British-based education in Malaysia. </p>

